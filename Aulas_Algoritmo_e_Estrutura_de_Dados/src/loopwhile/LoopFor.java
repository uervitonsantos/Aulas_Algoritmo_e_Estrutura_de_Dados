public class LoopFor {

	/**
	 * @param args
	 */
	public static void main(String[] args) {

		int numero = 21;
		int soma = 0;

		for (int i = 1; i <= numero; soma += i++) {
			System.out.println("Valor da soma: " + soma + " valor de i: " + i);

		}

	}

}
